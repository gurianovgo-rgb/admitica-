/**
 * Rich, per-university editorial content for the Detail page.
 * Keyed by university id (data/programs.js). Only universities present here
 * render the extended profile accordion + FAQ; others fall back to the
 * generic Detail layout. Same block scaffold for every uni — empty fields
 * just don't render, so other universities can be filled later.
 *
 * Tone: neutral / encyclopedic (no «ты»/«вы»). RU = highlighted note for
 * applicants from Russia.
 */

export interface UniSection {
  /** Section heading inside the accordion. */
  title: string
  /** Intro paragraph(s). */
  body?: string
  /** Compact fact list (teal-check bullets). */
  facts?: string[]
  /** Highlighted note for applicants from Russia. */
  ru?: string
  /** Verdict block. */
  pros?: string[]
  cons?: string[]
  /** Muted closing line. */
  note?: string
  /** Render the whole section with an accent marker (used for the Russia block). */
  accent?: boolean
}

export interface UniContent {
  /** Quick-fact chips under the header. */
  chips: string[]
  sections: UniSection[]
  faq: { q: string; a: string }[]
}

